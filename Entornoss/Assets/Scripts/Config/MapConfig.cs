using System;
using UnityEngine;

/// <summary>
/// Configuración de diseño de nivel seleccionable desde el menú.
/// Los anillos son fijos y nombrados. Solo se configuran parámetros de gameplay,
/// sin referencias a prefabs (esas permanecen en LevelGenerator).
/// </summary>
[CreateAssetMenu(fileName = "NewMapConfig", menuName = "Game/Config/Map Config")]
public class MapConfig : ScriptableObject
{
    [Header("Identificación")]
    public string mapName = "Mapa sin nombre";
    [TextArea(2, 4)]
    public string mapDescription = "";

    [Header("Sala del tesoro (siempre activa)")]
    [Range(3, 20)]
    public int treasureRoomSize = 7;

    [Header("Anillos intermedios (opcionales)")]
    public DecoratedRoomConfig decoratedRoom;
    public TileRoomConfig tileRoom;
    public WoodRoomConfig woodRoom;
    public BrokenTileRoomConfig brokenTileRoom;
    public CastleYardConfig castleYard;

    [Header("Bosque exterior (siempre activo)")]
    public ForestRingConfig outerForest;

    [Header("Probabilidades de drops")]
    public EnemyDropConfig dragonDropConfig;
    public EnemyDropConfig goatDropConfig;
}

